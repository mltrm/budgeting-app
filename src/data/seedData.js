export function buildSeedExpenses() {
  return RAW.map(([date, supplier, category, amount, description]) => ({
    id: crypto.randomUUID(),
    date: pd(date),
    supplier,
    category,
    amount: pa(amount),
    description: description || '',
    created_at: Date.now()
  })).reverse(); // newest first
}

export function buildSeedSuppliers() {
  const seen = new Set();
  return RAW
    .map(r => r[1])
    .filter(name => { if (seen.has(name)) return false; seen.add(name); return true; })
    .map(name => ({ id: crypto.randomUUID(), name }));
}

